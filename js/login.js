$(document).ready(function(){
    $('input[type="submit"]').hover(function(){
      $(this).css("color", "yellow");
      }, function(){
      $(this).css("color", "black");
    });
  });
  

$(document).ready(function(){
  $('input[type="text"], input[type="Password"]').hover(function(){
    $(this).css("color", "yellow");
    }, function(){
    $(this).css("color", "white");
  });
  });
  